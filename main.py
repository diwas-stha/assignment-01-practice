num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

def addition():
    return num1 + num2

def subtraction():
    return num1 - num2

def multiplication():
    return num1 * num2


print("The sum is:", addition())
print("The difference is:", subtraction())
print("The product is:", multiplication())
